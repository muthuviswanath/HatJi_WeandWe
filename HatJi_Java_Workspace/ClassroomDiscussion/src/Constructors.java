
public class Constructors {
/*
 * Name of the constructor must be the same as its class
 * Every class comes with an automatic constructor called default constructor
 * The access specifier of the default constructor is same as its class
 * A constructor can have higher or lower visibility as well
 * A constructor cannot be static or final or abstract
 * A constructor is not involved in inheritance
 * A constructor can be overloaded and the overloading rules are applied
 * The constructor serves two major purposes:
 * 	1. Initialize the instance variables
 *  2. Involve in object creation (Instantiation)
 * A constructor can be parameterized or non-parameterized
 * A constructor cannot have any return type not even void
 * A constructor is called explicitly during object creation
 * A constructor can be chained locally or can be chained during inheritance
 * Cyclic chaining is not allowed  
 * */
	
}
